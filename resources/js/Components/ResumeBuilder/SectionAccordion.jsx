export default function SectionAccordion({ section, index, total, onChange, onMoveUp, onMoveDown, onRemove, defaultOpen = false }) {
    const icon = sectionIcons[section.type] || sectionIcons.custom;

    return (
        <Disclosure defaultOpen={defaultOpen}>
            {({ open }) => (
                <div className="bg-white rounded-xl border border-gray-200 overflow-hidden">
                    <Disclosure.Button className="w-full flex items-center justify-between px-4 py-3 text-left hover:bg-gray-50 transition-colors">
                        <div className="flex items-center gap-2.5">
                            <span className="text-gray-500">{icon}</span>
                            <span className="text-sm font-semibold text-gray-900">{section.label}</span>
                        </div>
                        <div className="flex items-center gap-1">
                            {index > 0 && (
                                <button
                                    type="button"
                                    onClick={(e) => { e.stopPropagation(); onMoveUp(); }}
                                    className="p-1 text-gray-400 hover:text-gray-600 hover:bg-gray-100 rounded"
                                    title="Move up"
                                >
                                    <svg className="w-3.5 h-3.5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                                        <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M5 15l7-7 7 7" />
                                    </svg>
                                </button>
                            )}
                            {index < total - 1 && (
                                <button
                                    type="button"
                                    onClick={(e) => { e.stopPropagation(); onMoveDown(); }}
                                    className="p-1 text-gray-400 hover:text-gray-600 hover:bg-gray-100 rounded"
                                    title="Move down"
                                >
                                    <svg className="w-3.5 h-3.5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                                        <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M19 9l-7 7-7-7" />
                                    </svg>
                                </button>
                            )}
                            {section.type !== 'personal' && (
                                <button
                                    type="button"
                                    onClick={(e) => { e.stopPropagation(); onRemove(); }}
                                    className="p-1 text-gray-400 hover:text-red-500 hover:bg-red-50 rounded"
                                    title="Remove section"
                                >
                                    <svg className="w-3.5 h-3.5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                                        <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M19 7l-.867 12.142A2 2 0 0116.138 21H7.862a2 2 0 01-1.995-1.858L5 7m5 4v6m4-6v6m1-10V4a1 1 0 00-1-1h-4a1 1 0 00-1 1v3M4 7h16" />
                                    </svg>
                                </button>
                            )}
                            <svg className={`w-4 h-4 text-gray-400 transition-transform duration-200 ml-1 ${open ? 'rotate-180' : ''}`} fill="none" stroke="currentColor" viewBox="0 0 24 24">
                                <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M19 9l-7 7-7-7" />
                            </svg>
                        </div>
                    </Disclosure.Button>

                    <Transition
                        enter="transition duration-200 ease-out"
                        enterFrom="transform -translate-y-2 opacity-0"
                        enterTo="transform translate-y-0 opacity-100"
                        leave="transition duration-150 ease-in"
                        leaveFrom="transform translate-y-0 opacity-100"
                        leaveTo="transform -translate-y-2 opacity-0"
                    >
                        <Disclosure.Panel className="px-4 pb-4 border-t border-gray-100">
                            <div className="pt-4">
                                <SectionForm
                                    type={section.type}
                                    data={section.data}
                                    label={section.label}
                                    onChange={(data) => onChange({ ...section, data })}
                                    onLabelChange={section.type === 'custom' ? (label) => onChange({ ...section, label }) : undefined}
                                />
                            </div>
                        </Disclosure.Panel>
                    </Transition>
                </div>
            )}
        </Disclosure>
    );
}
